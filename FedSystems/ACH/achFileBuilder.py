class ACHEntry:
    def __init__(self):
        self.record_type_code = None
        self.transaction_code = None
        self.receiving_dfi_identification = None
        self.check_digit = None
        self.dfi_account_number = None
        self.amount_cents = None
        self.individual_id_number = None
        self.individual_name = None
        self.discretionary_data = None 
        self.addenda_record_indicator = None 
        self.trace_number = None
        self.addenda = []

    def add_addenda(self, addenda):
        self.addenda.append(addenda)

    def parse_record(self, line):
        self.record_type_code = line[0]
        self.transaction_code = line[1:3]
        self.receiving_dfi_identification = line[3:11].strip()
        self.check_digit = line[11]
        self.dfi_account_number = line[12:29].strip()
        self.amount_cents = int(line[29:39]) # Amount in cents, convert to dollars by dividing by 100
        self.individual_id_number = line[39:54].strip()
        self.individual_name = line[54:76].strip()
        self.discretionary_data = line[76:78].strip() # Note: This field is optional and can be used by the originator or ODFI for any purpose they choose. It may contain information that is relevant to the processing of the entry, but it is not standardized and may not be used by all originators or ODFIs.
        self.addenda_record_indicator = line[78] # Note: This field indicates whether there are addenda records associated with this entry. If it is '1', there is at least one addenda record; if it is '0', there are no addenda records.
        self.trace_number = line[79:94].strip() # Note: This field is critical for tracking the transaction through the ACH network and should be unique for each entry. It is often generated by the ODFI and may include a combination of the ODFI's routing number and a sequential number to ensure uniqueness.
        self.addenda = [] # This will hold any addenda records associated with this entry
        return self

    def add_record(self, record : dict):
        """
        Adds a record to the entry.
        record = {
            'record_type_code': None,
            'transaction_code': None,
            'receiving_dfi_identification': None,
            'check_digit': None,
            'dfi_account_number': None,
            'amount_cents': None,
            'individual_id_number': None,
            'individual_name': None,
            'discretionary_data': None, 
            'addenda_record_indicator': None, 
            'trace_number': None
        }
        """
        self.record_type_code = record['record_type_code']
        self.transaction_code = record['transaction_code']
        self.receiving_dfi_identification = record['receiving_dfi_identification']
        self.check_digit = record['check_digit']
        self.dfi_account_number = record['dfi_account_number']
        self.amount_cents = record['amount_cents']
        self.individual_id_number = record['individual_id_number']
        self.individual_name = record['individual_name']
        self.discretionary_data = record['discretionary_data']
        self.addenda_record_indicator = record['addenda_record_indicator']
        self.trace_number = record['trace_number']
        return self
    
    def add_record(self, record_type_code, transaction_code, receiving_dfi_identification, check_digit, dfi_account_number, amount_cents, individual_id_number, individual_name, discretionary_data, addenda_record_indicator, trace_number):
        self.record_type_code = record_type_code
        self.transaction_code = transaction_code
        self.receiving_dfi_identification = receiving_dfi_identification
        self.check_digit = check_digit
        self.dfi_account_number = dfi_account_number
        self.amount_cents = amount_cents
        self.individual_id_number = individual_id_number
        self.individual_name = individual_name
        self.discretionary_data = discretionary_data
        self.addenda_record_indicator = addenda_record_indicator
        self.trace_number = trace_number
        return self

class ACHAddenda:
    def __init__(self):
        self.record_type_code = None
        self.addenda_type_code = None
        self.payment_related_information = None
        self.addenda_sequence_number = None
        self.entry_detail_sequence_number = None

    def parse_record(self, line):
        self.record_type_code = line[0]
        self.addenda_type_code = line[1:3]
        self.payment_related_information = line[3:83].strip() # Note: This is a free-form alphanumeric field that can contain any information related to the payment. It is often used to provide additional details about the transaction, such as invoice numbers, customer references, or other relevant information that can help the receiver understand the purpose of the payment.
        self.addenda_sequence_number = line[83:87].strip() # Note: This field indicates the sequence of addenda records associated with an entry. Hovewer the PPD CCD and WEB only allow one addenda record, so this field should be '0001' for those SEC codes. For other SEC codes that allow multiple addenda records, this field should be incremented for each additional addenda record associated with the same entry.
        self.entry_detail_sequence_number = line[87:94].strip() # Note: This field should match the last 7 digits of the trace number in the corresponding entry detail record to ensure proper association between the addenda and its entry.
        return self

    def add_record(self, record : dict):
        """
        Adds a record to the addenda.
        record = {
            'record_type_code': None,
            'addenda_type_code': None,
            'payment_related_information': None,
            'addenda_sequence_number': None,
            'entry_detail_sequence_number': None
        }
        """
        self.record_type_code = record['record_type_code']
        self.addenda_type_code = record['addenda_type_code']
        self.payment_related_information = record['payment_related_information']
        self.addenda_sequence_number = record['addenda_sequence_number']
        self.entry_detail_sequence_number = record['entry_detail_sequence_number']
        return self

    def add_record(self, record_type_code, addenda_type_code, payment_related_information, addenda_sequence_number, entry_detail_sequence_number):
        self.record_type_code = record_type_code
        self.addenda_type_code = addenda_type_code
        self.payment_related_information = payment_related_information
        self.addenda_sequence_number = addenda_sequence_number
        self.entry_detail_sequence_number = entry_detail_sequence_number
        return self

